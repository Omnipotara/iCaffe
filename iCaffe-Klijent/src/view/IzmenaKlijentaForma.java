    public void obradiPromenu(boolean izmenjeno) {
        if (azuriranjeSifre) {
            if (izmenjeno) {
                Kontroler.getInstance().getKf().setM(izmenjen);
                JOptionPane.showMessageDialog(this, "Uspesno ste promenili lozinku!");
            } else {
                JOptionPane.showMessageDialog(this, "Lozinka nije promenjena, problem sa serverom.");
                return;
            }
        } else {
            if (izmenjeno) {
                Kontroler.getInstance().getKf().setM(izmenjen);
                JOptionPane.showMessageDialog(this, "Uspesno ste promenili username!");
            } else {
                JOptionPane.showMessageDialog(this, "Username nije promenjen, zauzet je ili postoji problem sa serverom.!");
                return;
            }
        }
        this.m = Kontroler.getInstance().getKf().getM();
        Kontroler.getInstance().getKf().osveziTitle();
    }
