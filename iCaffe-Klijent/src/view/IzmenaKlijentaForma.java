    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        KlijentskiZahtev kz = new KlijentskiZahtev();
        

        if (!txtStariPassword.getText().equals(m.getPassword())) {
            JOptionPane.showMessageDialog(this, "Stari password nije ispravan.");
            return;
        }

        Musterija izmenjen = new Musterija();
        this.izmenjen = izmenjen;
        izmenjen.setId(m.getId());
        izmenjen.setEmail(m.getEmail());
        izmenjen.setKategorijaMusterije(m.getKategorijaMusterije());
        izmenjen.setPreostaloVreme(m.getPreostaloVreme());

        if (azuriranjeSifre) {
            String password = txtNoviPassword.getText();

            if (!password.equals(m.getPassword())) {
                if (password == null || password.length() <= 5) {
                    JOptionPane.showMessageDialog(this, "Password koji ste uneli nije validnog formata. [mora biti duzi od 5 karaktera]");
                    return;
                }
                izmenjen.setUsername(m.getUsername());
                izmenjen.setPassword(password);

                kz.setOperacija(Operacija.AZURIRANJE_PASSWORD);
                kz.setParam(izmenjen);

            } else {
                JOptionPane.showMessageDialog(this, "Niste promenili password.");
                return;
            }

            izmenjen.setPassword(password);
        } else {
            String username = txtUsername.getText().trim();

            if (!username.equals(m.getUsername())) {
                if (username == null || username.length() < 3) {
                    JOptionPane.showMessageDialog(this, "Username koji ste uneli nije validnog formata.");
                    return;
                }
                izmenjen.setPassword(m.getPassword());
                izmenjen.setUsername(username);

                kz.setOperacija(Operacija.AZURIRANJE_USERNAME);
                kz.setParam(izmenjen);

            } else {
                JOptionPane.showMessageDialog(this, "Niste promenili username.");
                return;
            }
        }

        try {
            Komunikacija.getInstance().posaljiZahtev(kz);
        } catch (IOException ex) {
            Logger.getLogger(IzmenaKlijentaForma.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_jButton1ActionPerformed

